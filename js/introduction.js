let currentItemIndex = 0; // Indeks item saat ini

const leftArrow = document.getElementById("left-arrow");
const rightArrow = document.getElementById("right-arrow");
const currentItem = document.getElementById("current-item");
const homeButton = document.getElementById("home-button");
const backButton = document.getElementById("back-button");

function updateItemContent() {
  const item = items[currentItemIndex];
  currentItem.innerHTML = `
                <div class="item" id="current-item">
                <div class="content-container">
                    <div class="course-content" id="course-content">
                        <h1 class="course-title" style="margin-top: 25px; color: aliceblue;">
                        ${item.title}
                        </h1>
                        <div class="course-content-text"
                            style="margin: 0 30px; text-align: justify; color: aliceblue; line-height: 1.6; font-size: 18px; overflow: hidden; max-height: 400px; -webkit-overflow-scrolling: touch; scrollbar-width: none; -ms-overflow-style: none;">
                            ${
                              currentItemIndex === 4
                                ? '<img src="assets/button/lets-play.webp" class="item" id="clickable-image" alt="Clickable Image" style="margin-top: 20px;">'
                                : item.description
                            }
                        </div>
                    </div>
                </div>
            </div>
    `;

  // Tambahkan event listener untuk gambar tombol pada item ke-5
  if (currentItemIndex === 4) {
    document.getElementById("clickable-image").addEventListener("click", () => {
      window.location.href = "materi.html";
    });
  }

  // Sembunyikan scrollbar
  const courseContentText = document.querySelector(".course-content-text");
  courseContentText.style.overflowY = "scroll";
  courseContentText.style.scrollbarWidth = "none"; // Firefox
  courseContentText.style.msOverflowStyle = "none"; // Internet Explorer 10+
  courseContentText.style.webkitScrollbar = "none"; // Webkit browsers
}

// Fungsi untuk menampilkan item berikutnya
function showNextItem() {
  currentItem.classList.remove(
    "slide-in-left",
    "slide-in-right",
    "slide-out-left",
    "slide-out-right"
  );
  currentItem.classList.add("slide-out-left");

  // Nonaktifkan pointer events
  currentItem.style.pointerEvents = "none";

  setTimeout(() => {
    currentItemIndex = (currentItemIndex + 1) % items.length;
    updateItemContent();
    currentItem.classList.remove("slide-out-left");
    currentItem.classList.add("slide-in-right");

    // Aktifkan kembali pointer events setelah animasi selesai
    currentItem.style.pointerEvents = "auto";
  }, 500); // Sesuaikan dengan durasi animasi Anda
}

// Fungsi untuk menampilkan item sebelumnya
function showPreviousItem() {
  currentItem.classList.remove(
    "slide-in-left",
    "slide-in-right",
    "slide-out-left",
    "slide-out-right"
  );
  currentItem.classList.add("slide-out-right");

  // Nonaktifkan pointer events
  currentItem.style.pointerEvents = "none";

  setTimeout(() => {
    currentItemIndex = (currentItemIndex - 1 + items.length) % items.length;
    updateItemContent();
    currentItem.classList.remove("slide-out-right");
    currentItem.classList.add("slide-in-left");

    // Aktifkan kembali pointer events setelah animasi selesai
    currentItem.style.pointerEvents = "auto";
  }, 500); // Sesuaikan dengan durasi animasi Anda
}

// Tambahkan event listener untuk panah kiri dan kanan
leftArrow.addEventListener("click", showPreviousItem);
rightArrow.addEventListener("click", showNextItem);

// Update konten item saat halaman dimuat
updateItemContent();

